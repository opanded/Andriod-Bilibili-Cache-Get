    @classmethod
    def random(cls, category: str) -> str:
        """随机获取指定类别的颜文字"""
        messages: List[str] = getattr(cls, category.upper(), [])
        return random.choice(messages) if messages else ""

    @classmethod
    def with_mascot(cls, category: str, mascot: str = "rabbit_frog") -> str:
        """带吉祥物的颜文字"""
        base_msg = cls.random(category)
        if mascot == "rabbit_frog":
            return f"🐰🐸 {base_msg}"
        elif mascot == "donut":
            return f"� {base_msg}"
        return base_msg
